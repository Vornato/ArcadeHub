function distortPhrase(phrase, index) {
  const swaps = [
    ["დრო", "საათი"],
    ["გული", "ძრავა"],
    ["სახლი", "ბაზა"],
    ["გზა", "ტრასა"],
    ["მეგობარი", "თანამგზავრი"],
    ["სიჩუმე", "დაბალი ხმაური"],
    ["გეგმა", "სქემა"],
    ["კარი", "შესასვლელი"]
  ];

  let result = phrase;
  const [from, to] = cycle(swaps, index);
  result = result.replace(from, to);

  if (index % 2 === 0) {
    result = result.split(" ").reverse().join(" ");
  }

  if (index % 3 === 0) {
    result = result.replace(/, /g, " და ");
  }

  if (index % 5 === 0) {
    result = result.replace(/\.$/, " დღეს.");
  }

  return result;
}

